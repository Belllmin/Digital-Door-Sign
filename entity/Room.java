package entity;

/**
 *
 * @author Sabrina
 */
public class Room {
     private int linkId;
    private String name;

    public Room(int linkId, String name) {
        this.linkId = linkId;
        this.name = name;
    }

    public int getLinkId() {
        return linkId;
    }

    public void setLinkId(int linkId) {
        this.linkId = linkId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
}
